package com.android.msu2u;

public class ButtonList {
	public int icon;
	public String title;
	public String subtitle;
	public ButtonList(){
		super();
	}
	
	public ButtonList(int icon, String title, String subtitle){
		super();
		this.icon = icon;
		this.title = title;
		this.subtitle = subtitle; 
	}
	
}